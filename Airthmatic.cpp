#include <bits/stdc++.h>
using namespace std;

int main()
{
    int n, m;
    cout << "Enter n and m : ";
    cin >> n >> m;
    cout << "Addition: " << n + m << endl;
    cout << "subtraction: " << n - m << endl;
    cout << "multiplication: " << n * m << endl;
    cout << "division: " << n / m << endl;
    cout << "modulus: " << n % m << endl;

    return 0;
}